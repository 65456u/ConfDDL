export interface RecurringDeadline {
  /** Month of the deadline using 1-12 */
  month: number;
  /** Day of the month using 1-31 */
  day: number;
  /** Hour of the day in 24-hour time */
  hour: number;
  /** Minute of the hour */
  minute: number;
  /** ISO 8601 timezone offset, e.g. +00:00, -12:00 */
  offset: string;
  /** Human readable label shown alongside the deadline */
  label: string;
  /** Mark true when the schedule is based on historic cadence */
  estimated?: boolean;
}

